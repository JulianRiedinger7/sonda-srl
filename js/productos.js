const productsContainer = document.querySelector(
	'.productos .products-container'
);
const productDescription = document.querySelector(
	'.productos .product-description'
);

const addProducts = () => {
	productsContainer.innerHTML += productos
		.map((producto, index) => {
			return `
    <div class='product-card'>
      <img src='${producto.img}' alt='${producto.nombre}'/>
      <div class='product-card-content'>
        <h3>${producto.nombre}</h3>
        <button data-index='${index}'>Conocer mas</button>
      </div>
    </div>
  `;
		})
		.join('');

	document.querySelectorAll('.product-card button').forEach((button) =>
		button.addEventListener('click', (evt) => {
			showProductDescription(evt.target.dataset.index);
		})
	);
};

const showProductDescription = (index) => {
	const product = productos[index];

	productsContainer.classList.add('d-none');
	productDescription.innerHTML = `
    <div>
      <img src='../images/back-arrow.png' alt='back-arrow' class='description-arrow'>
      <div class='product'>
        <div>
          <h2>${product.nombre}</h2>
          <img src='${product.img}' alt='${product.nombre}' />
        </div>
  
        <div>
          <p class='short'>${product.descripcion}</p>
        </div>
      </div>
			<h3>Catalogo:</h3>
			<p>Haz click <a href='${product.catalogo}' target='_BLANK'>Aqui</a> para obtener mas informacion acerca de ${product.nombre}</p>
    </div>
  `;

	productDescription.classList.remove('d-none');
	document
		.querySelector('.product-description .description-arrow')
		.addEventListener('click', () => {
			productDescription.classList.add('d-none');
			productsContainer.classList.remove('d-none');
		});
};

addProducts();
